//parameters


//parameter is written between the parenthesis in name of the function
//through parameter we can pass value to the function when th function is called
function sum(num) {
    console.log(num + 10);
}

sum(10);

//multiple parameters
function getSum(num1, num2) {
    console.log(num1 + num2);
};
getSum(10, 20);