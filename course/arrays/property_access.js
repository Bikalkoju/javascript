// Property access

//Every item in an array has a numbered position
//JavaScript starts counting items from 0, so the first item in an array will be at position 0

let fruits = ['apple', 'banana', 'grapes'];
console.log(fruits[0]);                 //output: apple
console.log(fruits[1]);                 //output: banana
console.log(fruits[2]);                 //output: grapes
console.log(fruits[3]);                 //output: undefined