//Push and Pop method

//push method 
//it is used add elements at the end of the array and return length of array after push
let arr = [1, 2, 3, 4, 5];
let arrPush= arr.push(6, 7);
console.log(arr);
console.log(arrPush);

//pop method
//removes the last element of the array and returns length of array before pop
let arr1 = [1, 2, 3, 4, 5];
let arrPop = arr1.pop();
console.log(arr1);
console.log(arrPop);