//Comparator Operator

let num = 7;

//less than(<) operator
if (num < 10) {
    console.log(`${num} is less than 10`);
} else {
    console.log(`${num} is greater than 10`);
}

//more than(>) operator
if (num > 4) {
    console.log(`${num} is greater than 4`);
} else {
    console.log(`${num} is less than 4`);
}

//less than or equal to(<=) operator
if (num <= 10) {
    console.log(`${num} is less than or equal to 10`);
} else {
    console.log(`${num} is greater than 10`);
}

//more than or equal to(>=) operator
if (num >= 6) {
    console.log(`${num} is greater than or equal to 6`);
} else {
    console.log(`${num} is less than 6`);
}

//is equal to (===) operator
let animal = 'cat';
if (animal === 'cat') {
    console.log('this is a true statement');
} else {
    console.log('This is a false statement');
}

//is not equal to(!==) operator
let myName = 'Bikal';
if (myName !== 'Bishal') {
    console.log('this is a true statement');
} else {
    console.log('This is a false statement');
}