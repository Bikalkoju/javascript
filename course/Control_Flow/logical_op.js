//Logical Operation

//types of logical operators
//1.and(&&) operator    //checks both values are true
//2.or(||) operator     //checks if either values are true

//and(&&) operator
let num1 = 5;
let num2 = 2;
if (num1 === 5 && num2 === 2) {
    console.log('This statement is true');
} else {
    console.log('This statement is false');
}

//or(||) operator
let animal = 'cat';
let fruit = 'apple';
if (animal === 'cat' || fruit === 'banana') {
    console.log('This statement is true');
} else {
    console.log('This statement is false');
}