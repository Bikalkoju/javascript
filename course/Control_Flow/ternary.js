//Chapter 9.7
//Ternary operator

//ternary operator is used to shorten if else operator
// condition ? if true execute this : if false execute this

//example 1
let isSleeping = true;
isSleeping ? console.log('He is sleeping') : console.log('He is awake');

//example 2
let num = 10;
num >= 15 ? console.log(`${num} is greater than or equal to 15`) : console.log(`${num} is smaller than 15`);; 