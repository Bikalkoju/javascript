num1 += 10;
console.log(num1);

//subtraction operator
console.log(20 - 15);

let num2 = 15;
num2 -= 10;
console.log(num2);

//division operator
console.log(50 / 10);

let num3 = 15;
num3 /= 3;
console.log(num3);

