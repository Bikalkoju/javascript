//Chapter 7
//Variables

//let variable
//you can change the value of it after the creation
let color = 'red';
let num = 10;
console.log(color);
console.log(num);

color = 'blue';
num = '5';
console.log(color); //color changes to blue
console.log(num);   //num changes to 5

//const variable
//you can not change the value of it after the creation
const animal = 'dog';
const num1 = 5;
console.log(animal);
console.log(num1);

animal = 'cat';
num1 = 4;
console.log(animal);    //animal cannot change to cat
console.log(num1);      //num1 cannot change to 4

//undefined variable
let myName;
let friend = undefined;
console.log(myName);
console.log(friend);